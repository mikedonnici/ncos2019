        <p class="has-text-grey">
          <i>
            "Elemental music is not just music. It is bound up with movement,
            dance and speech, and so it is a form of music in which one must
            participate, in which one is involved not as a listener but as a
            co-performer"
          </i>
          <br />- Carl Orff
        </p>

        <p>
          During this session, participants will gain an understanding of the
          skills Carl Orff developed in his method of teaching music to
          children. The session will begin with Orff's approach to teaching
          infants music using chant and body percussion to primary music with
          simple songs and instrumental accompaniment, then moving to stage 4
          and onwards with pentatonic scales and chords progressions.
        </p>

        <p>
          Participants will receive handouts for each arrangement, plus a bonus
          few to try out back at school. We will also discuss how to set up a
          music lesson and acquire instruments, as well as, ideas on how to use
          easy access instrumentation such as chairs and rubbish bins. By the
          end of the session, applicants will have a greater understanding and
          confidence in teaching music to any age group.
        </p>

        <p>
